    private void timedMessage(String str){
        Timer timer = new Timer(2000, e -> { 
        //timer (2sec) for the wrong input message
        // reset of the message when the timer is disabled
        jLabel1.setText("Παρακαλώ εισάγετε το όνομα της πόλης.");
        });
        timer.setRepeats(false); // executed only once
        jLabel1.setText(str);
        timer.start();
    }
    

