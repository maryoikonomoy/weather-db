    private void jButton_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_submitActionPerformed
        String cityinp = jTextField_input.getText().trim(); //takes the name of the city and remove the spaces from the beginning and the end
    
        if (!isValidCityName(cityinp)) {
            timedMessage("Μη έγκυρο όνομα πόλης");
            return;
        }
        String msg = null;

        try {
            if (wh.getCityAndWeather(cityinp, city, weather)) {
                // jumps to second screen, it displays the weather data for the selected city
                SecondScreen mySecondScreen = new SecondScreen(this.city,this.weather);
                mySecondScreen.setVisible(true);
                dispose();
            }else{
                timedMessage(wh.getMsg());
            }
        } catch (ParseException ex) {
            Logger.getLogger(screen.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
    
    //  Validation of city name
    private boolean isValidCityName(String cityName) {
        return cityName != null && !cityName.isEmpty() && !containsSpecialCharacters(cityName);
    }
    // Checking if the text contains special characters
    private boolean containsSpecialCharacters(String str) {
        return !str.matches("[a-zA-Z0-9\u0370-\u03FF ]*");
    }//GEN-LAST:event_jButton_submitActionPerformed

